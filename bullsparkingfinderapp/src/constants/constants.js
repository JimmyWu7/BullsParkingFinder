// Combine north and south campus parking lots into one array for easier management
const allParkingLots = [...northCampusParkingLots, ...southCampusParkingLots];

// Destination constant that maps parking lot names to their coordinates
export const Destination = allParkingLots.map((lot) => ({
  name: lot.name,
  coordinates: lot.coordinates,
}));

// Example: Google Maps hyperlink function using Destination
export const getGoogleMapsLink = (parkingLotName) => {
  // Find the parking lot by name
  const lot = Destination.find((d) => d.name === parkingLotName);
  if (!lot) {
    console.error(`Parking lot ${parkingLotName} not found`);
    return null;
  }
  // Create a Google Maps link using coordinates
  const [lat, lng] = lot.coordinates;
  return `https://www.google.com/maps?q=${lat},${lng}`;
};

